/** Get a field label in the given language */
export function t(key, lang = "mr") {
  return T.f[key]?.[lang] || T.f[key]?.mr || key;
}

/** Get a section title in the given language */
export function ts(key, lang = "mr") {
  return T.sec[key]?.[lang] || T.sec[key]?.mr || key;
}

/** Get a top-level string (societyName, formTitle, etc.) in the given language */
export function tb(key, lang = "mr") {
  return T[key]?.[lang] || T[key]?.mr || key;
}

/** Get options array for the given language */
export function tOpt(key, lang = "mr") {
  return T.opts[key]?.[lang] || T.opts[key]?.mr || [];
}

/**
 * Translate a single value from one language to another using the opts map.
 * E.g. translateHL("Married", "en", "mr", "vaivahik") → "विवाहित"
 */
export function translateHL(value, fromLang, toLang, optKey) {
  if (!value || fromLang === toLang) return value || "";
  const fromArr = T.opts[optKey]?.[fromLang];
  const toArr   = T.opts[optKey]?.[toLang];
  if (!fromArr || !toArr) return value;
  const idx = fromArr.indexOf(value);
  return idx >= 0 ? (toArr[idx] || value) : value;
}

/**
 * Translate an array of values from one language to another.
 * E.g. for CheckGroup options stored in Marathi, display in English.
 */
export function translateArrayHL(arr, fromLang, toLang, optKey) {
  if (!arr || !Array.isArray(arr)) return [];
  return arr.map(v => translateHL(v, fromLang, toLang, optKey));
}